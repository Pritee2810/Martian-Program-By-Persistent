package string;

public class StringDemo1 {
	public static void main(String[] args) {
		String s1 = "11";
		String s2 = "13";
		String s3 = s1+s2;
		System.out.println(s3);
		int a = Integer.parseInt(s1);
		int b = Integer.parseInt(s2);
		int c = a+b;
		System.out.println(c);
		System.out.println(s3);
	}
}
