package math;

public class Math1 {
	public static void main(String[] args) {
		System.out.println(Math.abs(-47.23));
		System.out.println(Math.abs(-0.00));    //this function tells the how much number is differ from 0
		System.out.println(Math.abs(4.8332));
		System.out.println();
		System.out.println(Math.ceil(7.8));
		System.out.println(Math.ceil(1.1));   //this method gives complete number next to given number
		System.out.println(Math.ceil(0.00));
		System.out.println();
		System.out.println(Math.floor(7.8));
		System.out.println(Math.floor(1.1));  //this method gives complete number previous to given number
		System.out.println(Math.floor(0.0));
		System.out.println();
		System.out.println(Math.max(4.6, -3.5));
		System.out.println(Math.max(0.0, 0.0));
		System.out.println();
		System.out.println(Math.min(4.2, -6.4));
		System.out.println(Math.min(0.0, 0.0));
		System.out.println();
		System.out.println(Math.pow(3, 3));
		System.out.println();
		System.out.println(Math.sqrt(9));
	}
}
